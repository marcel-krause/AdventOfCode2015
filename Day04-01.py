import sys
from hashlib import md5


#----------------------------#
#        Main Program        #
#----------------------------#

if __name__ == "__main__":
	# Print the welcome screen
	print('''
+------------------------------------+
|                                    |
|           Advent of Code           |
|         Day 04 - Riddle 01         |
|                                    |
+------------------------------------+
	''')

	# Secret code
	secretCode = "iwrupvqb"

	# Find the first hash with five leading zeros
	c = 1
	while True:
		hash = md5((secretCode + str(c)).encode('utf-8')).hexdigest()
		print(str(c) + ": " + hash)
		if hash[:5] in '000000':
			break
		c += 1

	sys.exit()
